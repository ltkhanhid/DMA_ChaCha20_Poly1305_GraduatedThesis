#!/usr/bin/env python3
"""Verify expected crypto values using Python cryptography library."""
import struct

from cryptography.hazmat.primitives.ciphers.aead import ChaCha20Poly1305
from cryptography.hazmat.primitives.ciphers import Cipher, algorithms

PT = b"Ladies and Gentlemen of the class of '99: If I could offer you o"
print(f"Plaintext ({len(PT)} bytes): {PT}")

# === Test 1: ChaCha20-only (RFC 8439 Section 2.4.2) ===
print("\n" + "="*60)
print("  ChaCha20 Encryption — RFC 8439 §2.4.2")
print("="*60)

key_ch = bytes(range(0x00, 0x20))
# ChaCha20 nonce format: counter(4B LE) + nonce(12B)
nonce_16 = struct.pack('<I', 1) + bytes([0x00,0x00,0x00,0x00, 0x00,0x00,0x00,0x4a, 0x00,0x00,0x00,0x00])

cipher = Cipher(algorithms.ChaCha20(key_ch, nonce_16), mode=None)
enc = cipher.encryptor()
ct_chacha = enc.update(PT) + enc.finalize()

expected_ct_chacha = bytes([
    0x6e, 0x2e, 0x35, 0x9a, 0x25, 0x68, 0xf9, 0x80,
    0x41, 0xba, 0x07, 0x28, 0xdd, 0x0d, 0x69, 0x81,
    0xe9, 0x7e, 0x7a, 0xec, 0x1d, 0x43, 0x60, 0xc2,
    0x0a, 0x27, 0xaf, 0xcc, 0xfd, 0x9f, 0xae, 0x0b,
    0xf9, 0x1b, 0x65, 0xc5, 0x52, 0x47, 0x33, 0xab,
    0x8f, 0x59, 0x3d, 0xab, 0xcd, 0x62, 0xb3, 0x57,
    0x16, 0x39, 0xd6, 0x24, 0xe6, 0x51, 0x52, 0xab,
    0x8f, 0x53, 0x0c, 0x35, 0x9f, 0x08, 0x61, 0xd8,
])

print(f"  Python CT : {ct_chacha.hex()}")
print(f"  Expected  : {expected_ct_chacha.hex()}")
print(f"  MATCH: {ct_chacha == expected_ct_chacha}")

# === Test 2: AEAD (RFC 8439 Section 2.8.2, 64B plaintext) ===
print("\n" + "="*60)
print("  AEAD ChaCha20-Poly1305 — RFC 8439 §2.8.2 (64B PT)")
print("="*60)

key_aead = bytes(range(0x80, 0xa0))
nonce_aead = bytes([0x07,0x00,0x00,0x00, 0x40,0x41,0x42,0x43, 0x44,0x45,0x46,0x47])
aad = bytes([0x50,0x51,0x52,0x53, 0xc0,0xc1,0xc2,0xc3, 0xc4,0xc5,0xc6,0xc7])

aead_cipher = ChaCha20Poly1305(key_aead)
ct_tag = aead_cipher.encrypt(nonce_aead, PT, aad)
ct_aead = ct_tag[:-16]
tag_aead = ct_tag[-16:]

expected_ct_aead = bytes([
    0xd3, 0x1a, 0x8d, 0x34, 0x64, 0x8e, 0x60, 0xdb,
    0x7b, 0x86, 0xaf, 0xbc, 0x53, 0xef, 0x7e, 0xc2,
    0xa4, 0xad, 0xed, 0x51, 0x29, 0x6e, 0x08, 0xfe,
    0xa9, 0xe2, 0xb5, 0xa7, 0x36, 0xee, 0x62, 0xd6,
    0x3d, 0xbe, 0xa4, 0x5e, 0x8c, 0xa9, 0x67, 0x12,
    0x82, 0xfa, 0xfb, 0x69, 0xda, 0x92, 0x72, 0x8b,
    0x1a, 0x71, 0xde, 0x0a, 0x9e, 0x06, 0x0b, 0x29,
    0x05, 0xd6, 0xa5, 0xb6, 0x7e, 0xcd, 0x3b, 0x36,
])

# Script's expected TAG
script_expected_tag = bytes([
    0x57, 0x72, 0x8d, 0x89, 0x81, 0x1f, 0x44, 0xe3,
    0x44, 0x9f, 0x0d, 0x1c, 0x25, 0xa3, 0xe9, 0x5e,
])

print(f"  Python CT  : {ct_aead.hex()}")
print(f"  Expected CT: {expected_ct_aead.hex()}")
print(f"  CT MATCH: {ct_aead == expected_ct_aead}")
print()
print(f"  Python TAG        : {tag_aead.hex()}")
print(f"  Script expected   : {script_expected_tag.hex()}")
print(f"  TAG MATCH: {tag_aead == script_expected_tag}")

if tag_aead != script_expected_tag:
    print(f"\n  *** TAG DIFFERS! The correct tag for 64B PT is: {tag_aead.hex()}")
    print(f"  *** Need to update AEAD_EXPECTED_TAG in fpga_demo.py")
    # Show byte-by-byte diff
    for i in range(16):
        if tag_aead[i] != script_expected_tag[i]:
            print(f"    Byte[{i:2d}]: python=0x{tag_aead[i]:02x}  script=0x{script_expected_tag[i]:02x}")

print("\n" + "="*60)
print("  DONE")
print("="*60)
